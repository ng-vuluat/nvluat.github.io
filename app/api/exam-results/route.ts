export async function GET(request: Request) {
  const { searchParams } = new URL(request.url)
  const sbd = searchParams.get("sbd")

  if (!sbd) {
    return new NextResponse("Số báo danh là bắt buộc", { status: 400 })
  }

  // Normalize the input by removing spaces and non-digit characters
  const normalizedSbd = sbd.replace(/\D/g, "")

  // Create a map for faster lookups
  const resultMap = new Map(examResults.map((result) => [result.sbd, result]))

  // Try different formats to find a match
  let result = resultMap.get(normalizedSbd)

  // If not found, try without leading zeros
  if (!result) {
    const withoutLeadingZeros = normalizedSbd.replace(/^0+/, "")
    for (const [key, value] of resultMap.entries()) {
      if (key.replace(/^0+/, "") === withoutLeadingZeros) {
        result = value
        break
      }
    }
  }

  // If still not found, try with partial matching (last digits)
  if (!result) {
    for (const [key, value] of resultMap.entries()) {
      if (key.endsWith(normalizedSbd)) {
        result = value
        break
      }
    }
  }

  if (result) {
    return NextResponse.json(result)
  } else {
    return new NextResponse("Không tìm thấy kết quả", { status: 404 })
  }
}
